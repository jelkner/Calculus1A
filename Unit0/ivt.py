import math


def f(x):
    return math.sqrt(x) - math.cos(x)


def g(x):
    return 4 * x ** -3 - x - sin(math.pi * x)


def ql3(x):
    return ((12 / x) - 3 * x) / (2 - 3 * x + x ** 2)


def ql4(x):
    return (2 * x **2 - 10 * x + 12) / (x ** 3 - 6 * x ** 2 + 9 * x)


def ql5(x):
    return (3 * x ** 3 + x ** 2) / (x ** 3 + x ** 2 + x)
